const findKeyByValue = function(list, show) {
  let arr = Object.entries(list);
  for (let item of arr) {
    if (item[1] === show) {
      return item[0];
    }
  }
  return undefined;
};

const bestTVShowsByGenre = {
  sci_fi: "The Expanse",
  comedy: "Brooklyn Nine-Nine",
  drama: "The Wire"
};

